/** strikethrough(t) — Red strikethrough text showing original error */
function strike(t) {
    return new TextRun({
        text: t,
        size: 19,
        font: '微软雅黑',
        color: C.RED,
        strike: true,
    });
}

/** correct(t) — Green bold text showing correction */
function correct(t) {
    return new TextRun({
        text: t,
        size: 19,
        font: '微软雅黑',
        color: C.GREEN,
        bold: true,
    });
}

/** normal(t) — Normal text run */
function normal(t) {
    return new TextRun({
        text: t,
        size: 19,
        font: '微软雅黑',
        color: C.BLACK,
    });
}

/** note(t) — Gray annotation note */
function note(t) {
    return new Paragraph({
        children: [
            new TextRun({
                text: '📝 ' + t,
                size: 17,
                font: '微软雅黑',
                color: C.GRAY,
                italics: true,
            }),
        ],
        spacing: { after: 40, line: 300 },
        indent: { left: 400 },
    });
}

/** pageLabel(n, title) — Page label */
function pageLabel(n, title) {
    return new Paragraph({
        children: [
            new TextRun({
                text: 'P' + String(n).padStart(2, '0') + '  ' + title,
                size: 24,
                font: '微软雅黑',
                bold: true,
                color: C.MAIN,
            }),
        ],
        spacing: { before: 300, after: 100 },
        border: {
            bottom: {
                style: BorderStyle.SINGLE,
                size: 1,
                color: C.MAIN,
            },
        },
    });
}

/** correctionRow(original, fixed, reason) — Single correction entry */
function correctionRow(original, fixed, reason) {
    return [
        new Paragraph({
            children: [strike('❌ ' + original)],
            spacing: { after: 30, line: 340 },
            indent: { left: 200 },
        }),
        new Paragraph({
            children: [correct('✅ ' + fixed)],
            spacing: { after: 30, line: 340 },
            indent: { left: 200 },
        }),
        note(reason),
        divider(),
    ];
}

/** compliancePass() — Show page passed review */
function compliancePass(reason) {
    return [
        greenCheck('本页合规检查通过'),
        note(reason || '术语、逻辑、合规均无问题'),
        divider(),
    ];
}

/** sectionTitle(t) — Major section divider */
function sectionTitle(t) {
    return [
        pageBreak(),
        h2(t, { before: 300, after: 150 }),
        divider(),
    ];
}

