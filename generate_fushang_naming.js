// 6维评估表
function dimTable(scores) {
    var dims = ['核心定位元素','传播记忆元素','商业适配元素','合规安全元素','文化底蕴元素','长效成长元素'];
    var headers = ['评估维度','得分','评级','评估说明'];
    var rows = [];
    for (var i = 0; i < dims.length; i++) {
        var s = scores[i];
        var star = '';
        for (var j = 0; j < 5; j++) star += (j < s.stars) ? '★' : '☆';
        rows.push([dims[i], star, s.grade, s.note]);
    }
    return dataTable(headers, rows);
}

